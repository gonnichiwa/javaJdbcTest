package carInfo.dto;

public class CarInfo {
	private int ciNum;
	private String ciName;
	private String ciMaker;
	private int ciPrice;
	public int getCiNum() {
		return ciNum;
	}
	public void setCiNum(int ciNum) {
		this.ciNum = ciNum;
	}
	public String getCiName() {
		return ciName;
	}
	public void setCiName(String ciName) {
		this.ciName = ciName;
	}
	public String getCiMaker() {
		return ciMaker;
	}
	public void setCiMaker(String ciMaker) {
		this.ciMaker = ciMaker;
	}
	public int getCiPrice() {
		return ciPrice;
	}
	public void setCiPrice(int ciPrice) {
		this.ciPrice = ciPrice;
	}
	
	
	
	
}
